def main():
    n,m = map(int,input().split())
    p = n//2
    e=top(n,m,p)
    bottom(e,m,p)

    
    
def top(n,m,p):
       
    mult = 1
    for i in range(p):        
        a = ".|."*mult
        print(a.center(m,'-'))
        mult+=2
    print("WELCOME".center(m,"-"))
    return mult

def bottom(mult,m,p):
    for i in range(p):
        mult-=2
        a = ".|."*mult
        print(a.center(m,'-'))
    
    
main()
